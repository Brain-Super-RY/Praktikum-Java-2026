public class Bingo {
    
    // Konstruktor untuk mencetak lagu Bingo
    public Bingo() {
        // Bait pertama - tanpa clap
        System.out.println("There was a farmer who had a dog,");
        System.out.println("And Bingo was his name-o.");
        System.out.println("B-I-N-G-O");
        System.out.println("B-I-N-G-O");
        System.out.println("B-I-N-G-O");
        System.out.println("And Bingo was his name-o.");
        System.out.println();
        
        // Bait kedua - 1 clap
        System.out.println("There was a farmer who had a dog,");
        System.out.println("And Bingo was his name-o.");
        System.out.println("(clap)-I-N-G-O");
        System.out.println("(clap)-I-N-G-O");
        System.out.println("(clap)-I-N-G-O");
        System.out.println("And Bingo was his name-o.");
        System.out.println();
        
        // Bait ketiga - 2 clap
        System.out.println("There was a farmer who had a dog,");
        System.out.println("And Bingo was his name-o.");
        System.out.println("(clap)-(clap)-N-G-O");
        System.out.println("(clap)-(clap)-N-G-O");
        System.out.println("(clap)-(clap)-N-G-O");
        System.out.println("And Bingo was his name-o.");
        System.out.println();
        
        // Bait keempat - 3 clap
        System.out.println("There was a farmer who had a dog,");
        System.out.println("And Bingo was his name-o.");
        System.out.println("(clap)-(clap)-(clap)-G-O");
        System.out.println("(clap)-(clap)-(clap)-G-O");
        System.out.println("(clap)-(clap)-(clap)-G-O");
        System.out.println("And Bingo was his name-o.");
        System.out.println();
        
        // Bait kelima - 4 clap
        System.out.println("There was a farmer who had a dog,");
        System.out.println("And Bingo was his name-o.");
        System.out.println("(clap)-(clap)-(clap)-(clap)-O");
        System.out.println("(clap)-(clap)-(clap)-(clap)-O");
        System.out.println("(clap)-(clap)-(clap)-(clap)-O");
        System.out.println("And Bingo was his name-o.");
        System.out.println();
        
        // Bait keenam - 5 clap (semua)
        System.out.println("There was a farmer who had a dog,");
        System.out.println("And Bingo was his name-o.");
        System.out.println("(clap)-(clap)-(clap)-(clap)-(clap)");
        System.out.println("(clap)-(clap)-(clap)-(clap)-(clap)");
        System.out.println("(clap)-(clap)-(clap)-(clap)-(clap)");
        System.out.println("And Bingo was his name-o.");
    }
}